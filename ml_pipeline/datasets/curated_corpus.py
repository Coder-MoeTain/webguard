def build_curated_labeled_dataframe(
    n_per_class: int = 400,
    random_state: int = 7,
) -> pd.DataFrame:
    """
    Sample payloads with replacement from curated lists and attach simulated HTTP metadata.
    """
    rng = random.Random(random_state)
    rows = []
    pools = {
        "sqli": _SQLI,
        "xss": _XSS,
        "csrf": _CSRF,
        "benign": _BENIGN,
    }
    for label, pool in pools.items():
        for i in range(n_per_class):
            payload = pool[rng.randrange(len(pool))]
            row = simulate_http_record(payload, label, seed=random_state + i + hash(label) % 997)
            rows.append(row)
    df = pd.DataFrame(rows)
    return df.sample(frac=1.0, random_state=random_state).reset_index(drop=True)
